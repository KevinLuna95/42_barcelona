int	ft_str_is_printable(char *str)
{
	int	c;
	int	res;

	res = 1;
	c = 0;
	while (str[c] != '\0')
	{
		if (str[c] >= 32 && str[c] <= 126)
			c++;
		else
		{
			res = 0;
			c++;
		}
	}
	return (res);
}
