int	ft_str_is_alpha(char *str)
{
	int	c;

	c = 0;
	while (str[c] != '\0')
	{
		if ((str[c] >= 1 && str[c] <= 64) || (str[c] >= 123 && str[c] <= 126)
			|| (str[c] >= 91 && str[c] <= 96))
			return (0);
		c++;
	}
	return (1);
}
