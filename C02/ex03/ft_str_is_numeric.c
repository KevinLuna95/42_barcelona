int	ft_str_is_numeric(char *str)
{
	int	c;

	c = 0;
	while (str[c] != '\0')
	{
		if ((str[c] >= 0 && str[c] <= 47) || (str[c] >= 58 && str[c] <= 126))
			return (0);
		c++;
	}
	return (1);
}
